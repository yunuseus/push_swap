#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	k;

	k = ft_strlen(s);
	i = 0;
	while (i <= k)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	return (0);
}
